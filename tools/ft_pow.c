#include "libft.h"

long	ft_pow(int nb, int e)
{
	int		i;
	long	total;

	i = 1;
	if (e == 0)
		return (1);
	total = nb;
	while (i < e)
	{
		total *= nb;
		i++;
	}
	return (total);
}
